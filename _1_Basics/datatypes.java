public class datatypes {
    public static void main(String[] args) {
        boolean flag = true;
        int a = 12;
        short s = 99;
        long l = 4526; 
        float f = 1234.2563f;
        double d = 789456.25634;
        char ch = 'a';
        String str = "aaditya";

        System.out.println("Its a boolean: "+ flag);
        System.out.println("Its a integer: "+ a);
        System.out.println("Its a short: "+ s);
        System.out.println("Its a long: "+ l);
        System.out.println("Its a float: "+ f);
        System.out.println("Its a double: "+ d);
        System.out.println("Its a character: "+ ch);
        System.out.println("Its a string: "+ str);
    }
}
